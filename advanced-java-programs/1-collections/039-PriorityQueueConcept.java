import java.util.PriorityQueue;

class PriorityQueueConcept {
    public static void main(String[] args) {

        // Creating a priority queue
        PriorityQueue numbers = new PriorityQueue();
        numbers.add(4);
        numbers.add(2);
        numbers.add(1);
        System.out.println("PriorityQueue: " + numbers);
    }
}
